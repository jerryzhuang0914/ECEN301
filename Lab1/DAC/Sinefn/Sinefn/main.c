/***  Include Files  *******************************************************/

#include "AT89C51AC3.h"
#include <math.h>



/**  Main Function  ********************************************************/
#define STEP 2048.0

void main()
{
	while (1)
	{
		for(int i =0; i<STEP;i++){
			int val = 127.0 * sinf(((2*PI)/STEP)*i) + 128.0;
			P1 = val;
		}
		
	}
}